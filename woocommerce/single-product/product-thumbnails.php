defined( 'ABSPATH' ) || exit;

// Note: `wc_get_gallery_image_html` was added in WC 3.3.2 and did not exist prior. This check protects against theme overrides being used on older versions of WC.
if ( ! function_exists( 'wc_get_gallery_image_html' ) ) {
	return;
}

global $product;

$attachment_ids = $product->get_gallery_image_ids();

if ( $attachment_ids && $product->get_image_id() ) {
	echo '<div class="single-product-thumbnails slick-centered owl-carousel owl-theme">';
	foreach ( $attachment_ids as $attachment_id ) {
		echo '<div class="img-wrapper" style="padding-left:7px; padding-right:7px"><img data-src="'.wp_get_attachment_url( $attachment_id  ).'" src="'.aq_resize(wp_get_attachment_url( $attachment_id  ),100,100,true).'" class="flex-active" width="100" height="100"></div>';
		//100x100
		// echo apply_filters( 'woocommerce_single_product_image_thumbnail_html', wc_get_gallery_image_html( $attachment_id ), $attachment_id ); // phpcs:disable WordPress.XSS.EscapeOutput.OutputNotEscaped
	}
	echo '</div>';
}
